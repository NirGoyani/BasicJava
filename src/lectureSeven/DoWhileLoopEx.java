package lectureSeven;

public class DoWhileLoopEx {
    public static void main(String[] args) {
        //While Loop - run the code again and again till the condition is true
                      // first it check the condition and then run the statement ( Entry Control Loop)

        //do while loop  -- first run the statement and then check the condition (Exit Control Loop)
                    //    --  this loop will run atleast one time

        int a = 6;  //start

        do {
            System.out.println("Nirav");
            a++;  //7
        }while (a<=5);  //false

    }
}
